#data type conversion bool in int/float/str

#bool value assignment
val = True

#bool to int conversion
val1 = int(val)
#print the int output
print("The int output is:", val1)
print(type(val1))

#bool to float conversion
val1 = float(val)
#print the float output
print("The float output is:", val1)
print(type(val1))

#bool to str conversion
val1 = str(val)
#print the str output
print("The str output is:", val1)
print(type(val1))

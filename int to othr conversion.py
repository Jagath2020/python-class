#data type type conversion int to float/bool/str

#int value assignment
val = 10

#int to float conversion
val1 = float(val)
#print the float output
print("The float output is:", val1)
print(type(val1))

#int to bool conversion
val1 = bool(val)
#print the bool output
print("The bool output is:", val1)
print(type(val1))

#int to str conversion
val1 = str(val)
#print the str output
print("The str output is:", val1)
print(type(val1))

#data type type conversion float to int/bool/str

#float value assignment
val = 10.52

#float to int conversion
val1 = int(val)
#print the int output
print("The int output is:", val1)
print(type(val1))

#float to bool conversion
val1 = bool(val)
#print the bool output
print("The bool output is:", val1)
print(type(val1))

#float to str conversion
val1 = str(val)
#print the str output
print("The str output is:", val1)
print(type(val1))
